// Fonction de traduction des ressources
window.POKOPIA_TRANSLATIONS.translateRessource = function(text) {
  if (!text) return text;
  const dict = window.POKOPIA_TRANSLATIONS.items;
  let result = text;
  // Trier par longueur décroissante pour éviter les remplacements partiels
  const keys = Object.keys(dict).sort((a, b) => b.length - a.length);
  for (const fr of keys) {
    if (result.includes(fr)) {
      result = result.split(fr).join(dict[fr]);
    }
  }
  return result;
};